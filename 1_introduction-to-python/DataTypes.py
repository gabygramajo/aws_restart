# tipo de dato entero
myValue = 1
print(str(myValue) + " is of the data type " + str(type(myValue)))

# tipo de dato flotante
myValue = 3.14
print(str(myValue) + " is of the data type " + str(type(myValue)))

# tipo de dato complejo
myValue = 5j
print(str(myValue) + " is of the data type " + str(type(myValue)))

# tipo de dato booleano
myValue = True
print(str(myValue) + " is of the data type " + str(type(myValue)))

myValue = False
print(str(myValue) + " is of the data type " + str(type(myValue)))